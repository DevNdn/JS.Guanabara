/* ----- ESTRUTURAS DE REPETIÇÃO

 *  1.  while (condição){
          }


 *  2.  do {
          } while (condição)
  
*/


var teste = 10;
var teste1 = 0;

while (teste1 <= teste) {
  console.log(`Fala cmg, vagabundo`);
  teste1 ++;
  
}

teste1 = 0;

do {
  console.log(`Eae, safado`);
  teste1 ++;  // teste1 ++ vai adicionado +1 à variavel
} while (teste1 <= teste);

